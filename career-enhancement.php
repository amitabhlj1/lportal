                        <h3>(LangJobs Contest - I Winner Article)</h3>
                        <h5>Abstract:</h5>        
                        <p>This article summarizes the rising significance of International languages in accordance to the ever rising employment opportunities in commerce and industry.</p>
                        <p>An Introduction to the origin of foreign language instruction in India:</p>
                        <p>1990's witnessed the rise of foreign language instruction in India. Foreign language instruction, since then has become an integral part of the college/university curriculum in India. Relationship with the classical languages originally defined the place of foreign language in the academic curriculum.</h5>
                        <p>At first, there was a notion among the natives that the modern languages (foreign languages) were not worthy a discipline to revert to books. To tackle this issue of reputation, the modern language departments modeled themselves after two respected disciplines namely:</p>
                        <p>
                            <ul>
                                <li>The philological study of languages (the history of language combined with the reading texts illustrating earlier phases of the language)</li>
                                <li>Classical studies (the analysis of literary texts that form part of the cannon)</li>
                            </ul>
                        </p>
                        
                        <h5>Importance and Depth of Learning a Foreign language</h5>
                        <p>The advantage of learning a foreign language is threefold:</p>
                        <ol>
                            <li>Academic/ Educational<br/>An extra language in the academic curriculum is always a boon. It accentuates ones cognition and though processes and a good three years or so of learning a foreign language is an eye catcher in your CV. Regardless of the choice of the career, a person in command of any foreign language has an edge over the others.</li>
                            <li>Professional<br/>Employees who can communicate in foreign languages and understand other cultures are always a blessing for any employer. You are like a bridge to an employer who would always connect to new clients if you know their language.</li>
                            <li>Personal<br/>A person who is openly receptive to new culture which comes with learning a new language is blessed with the ability to reinforce their own identity and thereby the self-confidence. Study of a new language, communication with the natives of the language and knowing about their culture is both a source of pleasure and personal enrichment. Knowledge of a foreign language will enables to compare and contrast cultural concepts.</li>
                        </ol>                  
                        <h5>Careers for Foreign Language Experts:</h5>
                        <p>If you are competent enough in the concerned foreign language, the opportunity never quits knocking at your door. The majority of the job opportunities pertain to these areas/fields:</p>
                        <ol>
                            <li>Technical and Engineering Positions</li>
                            <li>Media and Arts</li>
                            <li>Interpreters</li>
                            <li>Translation</li>
                            <li>International e.g. United Nations</li>
                            <li>Secretarial and Clerical Positions</li>
                            <li>Teaching</li>
                            <li>Travel and Tourism</li>
                            <li>Government and International</li>
                            <li>Services</li>
                        </ol>
                        <hr>
                        <ol>
                            <li>Technical and Engineering Positions <br/>
                            Companies with overseas association would need technical and engineering personnel with foreign language proficiency for titles like Research Worker, Technical Engineer, Quality Control Supervisor etc.</li>
                            <li>Media and Arts<br/>
                            This is another significant area for Foreign Language expert<br/><br/>

                            Audio and Visual Media:<br/>

                            Many international broadcasting companies are in search for translators with a strong linguistic background for their Radio/TV marketing departments in their respective countries.<br/><br/>

                            Subtilter:<br/> A Foreign language specialist can work as subtitlers and provide synchronized captions in the target language for the film or television dialogue.<br/><br/>

                            Voice over artist:<br/> Here language specialists voice is not replaced by that of the actors, but a revoicing like that of a narration or commentary or a simple reading of the dialogue.<br/><br/>

                            Transcriptionist:<br/> Converts sounds and words of a spoken message and converts it into written translation.<br/><br/>

                            Language Engineer:<br/> A combination of foreign language and software skills is essential<br/><br/>

                            Print Media:<br/>

                            Magazines, newspapers, publication houses are always in need of language experts. Language specialists can work as copywriters, editors, editorial assistants in the publishing houses.<br/><br/>

                            Lexicographer:<br/> A bilingual or multilingual can work on developing entry or definition of a word, term or concept to be added to the dictionary.</li>
                            <li>Interpreters<br/>

                            Interpreters are specialists who deal with oral communication. Interpreters can be employed by Governments, International Agencies, Conferences, Publishers and other business organizations. Language for an interpreter is like the Reflex Action in the human body. They have to take quick and apt decision in the source language and render it simultaneously in the target language. Simultaneous Interpreter should never be short of ideas or words since the speaker continues to talk even when the Interpreter Interprets. In Consecutive Interpretation, the speaker and the Interpreter takes turn to speak. Sharp memory is another key asset of the Interpreter.<br/><br/>

                            Some other kinds of Interpreters are:<br/>

                            Conference Interpreters- serve at meetings, seminars and discussions<br/>

                            Escort Interpreters- escort visitors or delegates and interprets usually in a informal situation. They are quite a many in number<br/>

                            Courtroom Interpreters- usually happens in the court room. They usually implements consecutive interpreting, but sometimes they have to implement simultaneous interpretation or sight translation<br/>

                            Telephonic Interpreters, Community Interpreters</li>
                            <li>
                                Translation<br/>

                                Translators usually are specialized for translation in the technical or literary fields. Similar to that of the Interpreter, the translator can also work as staff in Governments, International Agencies, Conferences, Publishers and other business organizations.
                            </li>
                            <li>
                                International e.g. United Nations<br/>

                                The UN is the largest employer of language specialists which includes translators, editors and interpreters. Bilinguals have an edge here and can also work as economists, education specialists, financial analysts, public information officers, librarians, technical specialists in the fields of agriculture and industries, engineers and statisticians. The United Nations conducts a yearly competive examination followed by an interview for the recruitment of Foreign Language specialists
                            </li>
                            <li>
                                Secretarial and Clerical Positions<br/>

                                Bilinguals or Multilingual (like Indians) have an edge here. Many employment agencies advertise every now and then for the posts of Bilingual or Multilingual secretaries and typists. Stenography, Translation and correspondence services include Secretaries. This should give an insight into the matter that the duties are vast and only a very competent language professional can fit the bill.
                            </li>
                            <li>
                                Teaching<br/>

                                Foreign Language teachers are always in a great demand. If you specialize in languages such as French, Spanish, German, Japanese, Chinese, you will never be short of teaching offers. There are a lot of people who teach English as a foreign language.
                            </li>
                            <li>
                                Travel and Tourism<br/>

                                Transportation, Hotels and Motels, Tours Conductors need the service of Foreign language experts to interact with their Plum Customers. This industry fetches the language expert well enough.
                            </li>
                            <li>
                                Government and International<br/>

                                Federal Bureau of Investigation (FBI):<br/>

                                Linguists and Foreign Language experts can work as an agent with the FBI to conduct foreign counterintelligence investigations within the country and also to coordinate the domestic counterintelligence activities of other agencies in the intelligence community.<br/><br/>



                                National Security Agency (NSA):<br/>

                                Research Assistants, Communication experts and translators who are versed in foreign languages can work with NSA which is under the Department of Defense.
                            </li>
                            <li>
                                Services<br/>

                                Health Professionals especially Natural therapists would need the assistance of a language specialist to reach their voice to the foreign patients.<br/>

                                Library Science: The librarian may use language specialists to know what a foreign language book is all about.
                            </li>
                        </ol>
                        <h5>Conclusion:</h5>
                        <p>Learning a new language is like setting out for a new discovery which is more like an adventure. You will hardly be aware of what lies in store for you at the finishing point. But yes, there is much more to learning a language than learning its phonology, morphology, syntax and semantics, and definitely, its literature. Learning a language also means learning its socio-cultural background and also studying and accepting the culture of the other. It also means knowing or learning ones own culture from a new perspective.</p>
