                        <p>Chinese, like the other languages of the Sino-Tibetan family and is a tonal language. Chinese is written with thousands of distinctive characters called ideographs which have no relation to the sound of a word. In a large dictionary there are 40-50,000 characters. While a Chinese child learns about 2,000 characters by the time he is ten, it takes two or three times as many to be able to read a newspaper or novel. One kind of Chinese type-writer has 5,400 characters. The number of strokes required to draw a Chinese character can be as high as 33.</p>

                        <p>The majority of Chinese characters, however, consist of two elements ï¿½a signific, which indicates the meaning of a word, and a phonetic, which indicates the sound. The significs, or radicals, number 214 in Chinese, and indicate the class of objects to which the word belongs. For example, all words relating to wood, such as "tree" and "table," contain the "wood" radical. The phonetic consists of the character for a word whose meaning is totally unrelated to the word in question, but whose pronunciation happens to be the same. Thus the character for "ocean" consists of the signific "water" plus the phonetic "sheep," the word for "sheep" being pronounced the same as the word for "ocean."</p>

                        <p>Chinese has many dialects, two of them widely spoken are Mandarin spoken by majority of the population and Cantonese spoken by the rest of the population including Hong Kong. A given word may be quite different in Mandarin and Cantonese, but it would be written identically in the two dialects. Since the Chinese characters are also used in Japanese, each language, when written, is partially intelligible to a speaker of the other, despite the fact that the two spoken languages are totally dissimilar.</p>

