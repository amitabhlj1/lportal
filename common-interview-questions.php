                        <legend>Interview Tips</legend>
                        <h5>Interview Tips: General Questions / Common Questions</h5>        
                        <p>In India, most of Language Jobs are from BPO / KPO / IT /ITES industries where usually there are 5 rounds of interview for selecting any candidate for language expert jobs:</p>
                        <ol>
                            <li>HR Screening - Usually telephonic</li>
                            <li>Language Written Test / Aptitude Written Test - At company location</li>
                            <li>Client Interview / Language Oral Interview - Usually telephonic</li>
                            <li>Operations Round Interview - Personal Interview / Telephonic Interview</li>
                            <li>HR Interview to negotiate salary - Personal Interview / Telephonic Interview</li>
                        </ol>
                        <p>
                            In all the above rounds there are few common questions start the minute the interview commence, and to show that you are an exceptional candidate, you need to be prepared to answer not only the typical questions, but also the unexpected. You can expect questions regarding your qualifications, your academic preparation, career interests, experience, and ones that assess your personality. <br/><br/>

                            Here we are putting some common general questions that you may encounter in the interviews:
                        </p>
                        <ol>
                            <li class="red">Tell me about yourself / Introduce yourself
                                <p class="black">This is the most often asked question in interviews. You need to have a short statement prepared in your mind. Be careful that it does not sound rehearsed.<br/>

                                If you are a starter - limit it to your study-related items or if you are experienced limit it to work-related items unless instructed otherwise. Talk about things you have done / achievement and jobs you have held that relate to the position you are interviewing for. Start with your first job and reach to the recent one if you are experience. Similar sequence can be adopted for the fresher in terms of course starting from school to college. 
                               </p>
                            </li>
                            <li class="red">Why did you leave your last job?
                                <p class="black">Always be  positive regardless of the circumstances. Never refer to a major problem with management and never speak ill of supervisors, co-workers or the organization. If you do, you will be the one looking bad. Keep smiling and talk about leaving for a positive reason such as an opportunity, a chance to do something special or other forward-looking reasons. </p>
                            </li>
                            <li class="red">What experience do you have in this field?
                                <p class="black">Talk about specifics that relate to the position you are applying for. If you do not have specific experience, get as close as you can.</p>
                            </li>
                            <li class="red">Do you consider yourself successful?
                                <p class="black">You should always answer yes and briefly explain why. A good explanation is that you have set goals, and you have met some and are on track to achieve the others.</p>
                            </li>
                            <li class="red">What do co-workers say about you?
                                <p class="black">Be prepared with a quote or two from co-workers. Either a specific statement or a paraphrase will work.</p>
                            </li>
                            <li class="red">What do you know about this organization?
                                <p class="black">This question is one reason to do some research on the organization before the interview. Find out where they have been and where they are going. What are the current issues and who are the major players?</p>
                            </li>
                            <li class="red">What have you done to improve your knowledge in the last year?
                                <p class="black">Try to include improvement activities that relate to the job. A wide variety of activities can be mentioned as positive self-improvement. Have some good ones handy to mention.</p>
                            </li>
                            <li class="red">Are you applying for other jobs?
                                <p class="black">Be honest but do not spend a lot of time in this area. Keep the focus on this job and what you can do for this organization. Anything else is a distraction.</p>
                            </li>
                            <li class="red">Why do you want to work for this organization?
                                <p class="black">This may take some thought and certainly, should be based on the research you have done on the organization. Sincerity is extremely important here and will easily be sensed. Relate it to your long-term career goals.</p>
                            </li>
                            <li class="red">Do you know anyone who works for us?
                                <p class="black">Be aware of the policy on relatives working for the organization. This can affect your answer even though they asked about friends not relatives. Be careful to mention a friend only if they are well thought of.</p>
                            </li>
                            <li class="red">What kind of salary do you need?
                                <p class="black">A loaded question. A nasty little game that you will probably lose if you answer first. So, do not answer it. Instead, say something like, That's a tough question. Can you tell me the range for this position? In most cases, the interviewer, taken off guard, will tell you. If not, say that it can depend on the details of the job. Then give a wide range. </p>
                            </li>
                            <li class="red">Are you a team player?
                                <p class="black">You are, of course, a team player. Be sure to have examples ready. Specifics that show you often perform for the good of the team rather than for yourself are good evidence of your team attitude. Do not brag, just say it in a matter-of-fact tone. This is a key point.</p>
                            </li>
                            <li class="red">How long would you expect to work for us if hired?
                                <p class="black">Specifics here are not good. Something like this should work: I'd like it to be a long time. Or As long as we both feel I'm doing a good job.</p>
                            </li>
                            <li class="red">Have you ever had to fire anyone? How did you feel about that?
                                <p class="black">This is serious. Do not make light of it or in any way seem like you like to fire people. At the same time, you will do it when it is the right thing to do. When it comes to the organization versus the individual who has created a harmful situation, you will protect the organization. Remember firing is not the same as layoff or reduction in force.</p>
                            </li>
                            <li class="red">What is your philosophy towards work?
                                <p class="black">The interviewer is not looking for a long or flowery dissertation here. Do you have strong feelings that the job gets done? Yes. That's the type of answer that works best here. Short and positive, showing a benefit to the organization. </p>
                            </li>
                            <li class="red">If you had enough money to retire right now, would you?
                                <p class="black">Answer yes if you would. But since you need to work, this is the type of work you prefer. Do not say yes if you do not mean it.</p>
                            </li>
                            <li class="red">Have you ever been asked to leave a position?
                                <p class="black">If you have not, say no. If you have, be honest, brief and avoid saying negative things about the people or organization involved.</p>
                            </li>
                            <li class="red">Explain how you would be an asset to this organization
                                <p class="black">You should be anxious for this question. It gives you a chance to highlight your best points as they relate to the position being discussed. Give a little advance thought to this relationship.</p>
                            </li>
                            <li class="red">Why should we hire you?
                                <p class="black">Point out how your assets meet what the organization needs. Do not mention any other candidates to make a comparison.</p>
                            </li>
                            <li class="red">Tell me about a suggestion you have made
                                <p class="black">Have a good one ready. Be sure and use a suggestion that was accepted and was then considered successful. One related to the type of work applied for is a real plus.</p>
                            </li>
                            <li class="red">What irritates you about co-workers?
                                <p class="black">This is a trap question. Think real hard but fail to come up with anything that irritates you. A short statement that you seem to get along with folks is great.</p>
                            </li>
                            <li class="red">What is your greatest strength?
                                <p class="black">Numerous answers are good, just stay positive. A few good examples: Your ability to prioritize, Your problem-solving skills, Your ability to work under pressure, Your ability to focus on projects, Your professional expertise, Your leadership skills, Your positive attitude .</p>
                            </li>
                            <li class="red">Tell me about your dream job.
                                <p class="black">You cannot win if you be very specific. If you say the job you are contending for is it, you strain credibility. If you say another job is it, you plant the suspicion that you will be dissatisfied with this position if hired. The best is to stay genetic and say something like: A job where I love the work, like the people, can contribute and can't wait to get to work.</p>
                            </li>
                            <li class="red">Why do you think you would do well at this job?
                                <p class="black">Give several reasons and include skills, experience and interest. </p>
                            </li>
                            <li class="red">What are you looking for in a job?
                                <p class="black">See answer # 23</p>
                            </li>
                            <li class="red">What kind of person would you refuse to work with?
                                <p class="black">Do not be trivial. It would take disloyalty to the organization, violence or lawbreaking to get you to object. Minor objections will label you as a whiner.</p>
                            </li>
                            <li class="red">What is more important to you: the money or the work?
                                <p class="black">Money is always important, but the work is the most important. There is no better answer.</p>
                            </li>
                            <li class="red">What would your previous supervisor say your strongest point is?
                                <p class="black">There are numerous good possibilities: Loyalty, Energy, Positive attitude, Leadership, Team player, Expertise, Initiative, Patience, Hard work, Creativity, Problem solver</p>
                            </li>
                            <li class="red">Tell me about a problem you had with a supervisor
                                <p class="black">Biggest trap of all. This is a test to see if you will speak ill of your boss. If you fall for it and tell about a problem with a former boss, you may well below the interview right there. Stay positive and develop a poor memory about any trouble with a supervisor.</p>
                            </li>
                            <li class="red">What has disappointed you about a job?
                                <p class="black">Don't get trivial or negative. Safe areas are few but can include: Not enough of a challenge. You were laid off in a reduction Company did not win a contract, which would have given you more responsibility.</p>
                            </li>
                            <li class="red">Tell me about your ability to work under pressure.
                                <p class="black">You may say that you thrive under certain types of pressure. Give an example that relates to the type of position applied for.</p>
                            </li>
                            <li class="red">Do your skills match this job or another job more closely?
                                <p class="black">Probably this one. Do not give fuel to the suspicion that you may want another job more than this one.</p>
                            </li>
                            <li class="red">What motivates you to do your best on the job?
                                <p class="black">This is a personal trait that only you can say, but good examples are: Challenge, Achievement, Recognition</p>
                            </li>
                            <li class="red">Are you willing to work overtime? Nights? Weekends?
                                <p class="black">This is up to you. Be totally honest.</p>
                            </li>
                            <li class="red">How would you know you were successful on this job?
                                <p class="black">Several ways are good measures: You set high standards for yourself and meet them. Your outcomes are a success. Your boss tells you that you are successful.</p>
                            </li>
                            <li class="red">Would you be willing to relocate if required?
                                <p class="black">You should be clear on this with your family prior to the interview if you think there is a chance it may come up. Do not say yes just to get the job if the real answer is no. This can create a lot of problems later on in your career. Be honest at this point and save yourself future grief.</p>
                            </li>
                            <li class="red">Are you willing to put the interests of the organization ahead of your own?
                                <p class="black">This is a straight loyalty and dedication question. Do not worry about the deep ethical and philosophical implications. Just say yes.</p>
                            </li>
                            <li class="red">Describe your management style.
                                <p class="black">Try to avoid labels. Some of the more common labels, like progressive, salesman or consensus, can have several meanings or descriptions depending on which management expert you listen to. The situational style is safe, because it says you will manage according to the situation, instead of one size fits all.</p>
                            </li>
                            <li class="red">What have you learned from mistakes on the job?
                                <p class="black">Here you have to come up with something or you strain credibility. Make it small, well intentioned mistake with a positive lesson learned. An example would be working too far ahead of colleagues on a project and thus throwing coordination off.</p>
                            </li>
                            <li class="red">Do you have any blind spots?
                                <p class="black">Trick question. If you know about blind spots, they are no longer blind spots. Do not reveal any personal areas of concern here. Let them do their own discovery on your bad points. Do not hand it to them.</p>
                            </li>
                            <li class="red">If you were hiring a person for this job, what would you look for?
                                <p class="black">Be careful to mention traits that are needed and that you have.</p>
                            </li>
                            <li class="red">Do you think you are overqualified for this position?
                                <p class="black">Regardless of your qualifications, state that you are very well qualified for the position.</p>
                            </li>
                            <li class="red">How do you propose to compensate for your lack of experience?
                                <p class="black">First, if you have experience that the interviewer does not know about, bring that up: Then, point out (if true) that you are a hard working quick learner.</p>
                            </li>
                            <li class="red">What qualities do you look for in a boss?
                                <p class="black">Be generic and positive. Safe qualities are knowledgeable, a sense of humor, fair, loyal to subordinates and holder of high standards. All bosses think they have these traits.</p>
                            </li>
                            <li class="red">Tell me about a time when you helped resolve a dispute between others.
                                <p class="black">Pick a specific incident. Concentrate on your problem solving technique and not the dispute you settled.</p>
                            </li>
                            <li class="red">What position do you prefer on a team working on a project?
                                <p class="black">Be honest. If you are comfortable in different roles, point that out.</p>
                            </li>
                            <li class="red">Describe your work ethic.
                                <p class="black">Emphasize benefits to the organization. Things like, determination to get the job done and work hard but enjoy your work are good.</p>
                            </li>
                            <li class="red">What has been your biggest professional disappointment?
                                <p class="black">Be sure that you refer to something that was beyond your control. Show acceptance and no negative feelings.</p>
                            </li>
                            <li class="red">Tell me about the most fun you have had on the job.
                                <p class="black">Talk about having fun by accomplishing something for the organization.</p>
                            </li>
                            <li class="red">Do you have any questions for me?
                                <p class="black">Always have some questions prepared. Questions prepared where you will be an asset to the organization are good. How soon will I be able to be productive? and What type of projects will I be able to assist on? are examples.</p>
                            </li>
                        </ol>
